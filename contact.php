<script>
document.getElementById('contactForm').addEventListener('submit', function(e) {
    e.preventDefault();

    const btn   = document.getElementById('contactSubmitBtn');
    const msgEl = document.getElementById('contactMsg');

    btn.disabled  = true;
    btn.innerHTML = '<i class="fa-solid fa-spinner fa-spin me-2"></i>Sending...';
    msgEl.innerHTML = '';

    fetch('/DonaMart/actions/submit_contact.php', {
        method: 'POST',
        body: new FormData(this)
    })
    .then(r => r.text())
    .then(text => {
        let data;
        try { data = JSON.parse(text); }
        catch(e) { throw new Error('Server error. Please try again.'); }

        if (data.success) {
            msgEl.innerHTML = `
                <div style="background:#eaf3de;border:1px solid #c3e6a0;border-radius:10px;
                            padding:14px 18px;margin-bottom:16px;color:#3b6d11;font-size:14px;
                            display:flex;align-items:flex-start;gap:10px;">
                    <i class="fa-solid fa-circle-check" style="font-size:18px;flex-shrink:0;margin-top:1px;"></i>
                    <span>${data.message}</span>
                </div>`;
            document.getElementById('contactForm').reset();
        } else {
            msgEl.innerHTML = `
                <div style="background:#fcebeb;border:1px solid #f7c1c1;border-radius:10px;
                            padding:14px 18px;margin-bottom:16px;color:#a32d2d;font-size:14px;
                            display:flex;align-items:flex-start;gap:10px;">
                    <i class="fa-solid fa-circle-exclamation" style="font-size:18px;flex-shrink:0;margin-top:1px;"></i>
                    <span>${data.message}</span>
                </div>`;
        }
    })
    .catch(err => {
        msgEl.innerHTML = `
            <div style="background:#fcebeb;border:1px solid #f7c1c1;border-radius:10px;
                        padding:14px 18px;margin-bottom:16px;color:#a32d2d;font-size:14px;">
                <i class="fa-solid fa-circle-exclamation me-2"></i>${err.message}
            </div>`;
    })
    .finally(() => {
        btn.disabled  = false;
        btn.innerHTML = '<i class="fa-solid fa-paper-plane me-2"></i>Send Message';
        msgEl.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    });
});
</script>

